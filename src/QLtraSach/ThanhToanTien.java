package QLtraSach;

public class ThanhToanTien {
    private int soNgayThue;
    private int donGia;
    private int soNgayQuaHan;
    private int tienPhat;
    private int ThanhTien;
    private int moneyCoc;

    public ThanhToanTien() {
    }

    public int getSoNgayThue() {
        return soNgayThue;
    }

    public void setSoNgayThue(int soNgayThue) {
        this.soNgayThue = soNgayThue;
    }

    public int getDonGia() {
        return donGia;
    }

    public void setDonGia(int donGia) {
        this.donGia = donGia;
    }

    public int getSoNgayQuaHan() {
        return soNgayQuaHan;
    }

    public void setSoNgayQuaHan(int soNgayQuaHan) {
        this.soNgayQuaHan = soNgayQuaHan;
    }

    public int getTienPhat() {
        return tienPhat;
    }

    public void setTienPhat(int tienPhat) {
        this.tienPhat = tienPhat;
    }

    public int getThanhTien() {
        return ThanhTien;
    }

    public void setThanhTien(int thanhTien) {
        ThanhTien = thanhTien;
    }

    public int getMoneyCoc() {
        return moneyCoc;
    }

    public void setMoneyCoc(int moneyCoc) {
        this.moneyCoc = moneyCoc;
    }
}
